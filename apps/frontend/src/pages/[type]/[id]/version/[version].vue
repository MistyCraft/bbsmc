          );

          for (let i = 0; i < this.newFiles.length; i++) {
            formData.append(fileParts[i], new Blob([this.newFiles[i]]), this.newFiles[i].name);
          }

          // await useBaseFetch(`version/${this.version.id}/file`, {
          //   method: "POST",
